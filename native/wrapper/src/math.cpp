#include "../include/math.hpp"

double Math::Sum(double num1, double num2) {
    return sum(num1, num2);
}

double Math::Subtract(double num1, double num2) {
    return subtract(num1, num2);
}

double Math::Multiply(double num1, double num2) {
    return multiply(num1, num2);
}

double Math::Divide(double num1, double num2) {
    return divide(num1, num2);
}

double Math::Power(double base_value, double exponent_value) {
    return power(base_value, exponent_value);
}

double Math::SquareRoot(double num) {
    return square_root(num);
}